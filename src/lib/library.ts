function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
    .slice(0, 80);
}

function toFilename(author: string, title: string): string {
  const a = author.split('&')[0].trim().replace(/\s+/g, '_');
  const t = title.replace(/\s+/g, '_').replace(/[^a-zA-Z0-9_]/g, '');
  return `${a}_-_${t}.pdf`;
}

