// Build the library with generated IDs and filenames
const library: BookMeta[] = rawBooks.map((b) => ({
  id: slugify(b.title),
  title: b.title,
  author: b.author,
  filename: toFilename(b.author, b.title),
  ...(b.coverImage ? { coverImage: b.coverImage } : {}),
}));

// Handle duplicate IDs by appending index
const seen = new Map<string, number>();
for (const book of library) {
  const count = seen.get(book.id) || 0;
  if (count > 0) {
    book.id = `${book.id}-${count}`;
  }
  seen.set(book.id.replace(/-\d+$/, ''), count + 1);
}

export function getLibrary(): BookMeta[] {
  return library;
}

export function getBookById(id: string): BookMeta | undefined {
  return library.find((b) => b.id === id);
}

export function getBookUrl(book: BookMeta): string {
  return `/books/${book.filename}`;
}
